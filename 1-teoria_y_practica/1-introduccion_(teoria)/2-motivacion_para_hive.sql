/*  Motivación para HIVE
    ====================

    ● Hive se creó para reducir la complejidad de la programación MapReduce

    ● Cientos de líneas de código en un programa MapReduce pueden resumirse en una consulta de una sola línea.

    ● Fácil para los analistas sin experiencia en Java

    ● El compilador convierte las consultas en programas MapReduce

    ● Los analistas pueden utilizar sus conocimientos de SQL para escribir consultas   */