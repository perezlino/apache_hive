/*  FUNCIONES MATEMATICAS
    =====================

    CEIL
    ----

    La función CEIL devolverá el número entero más pequeño, mayor que el número que estamos pasando en 
    nuestro argumento.

    SELECT CEIL(9.5);

    Y obtenemos: 10

    ------------------------------------------------------------------------------------------------------------------------

    FLOOR
    -----

    La función FLOOR devolverá el número más grande, menor que el argumento entregado.

    SELECT FLOOR(10.9);

    Y obtenemos: 10

    ------------------------------------------------------------------------------------------------------------------------

    ROUND
    -----
    
    Las función ROUND redondeará nuestro número decimal.

    SELECT ROUND(123.456, 2);

    Y obtenemos: 123.46    

    ------------------------------------------------------------------------------------------------------------------------